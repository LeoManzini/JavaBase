public class ModifyingHelloWorld 
{
    public static void main(String args[]) 
    {
        // Printing a different message to the Hello World
        System.out.println("Hello my darling!!");

        // Printing a second line
        System.out.println("Goodbye!!");

        // Printing two lines with one line code
        System.out.println("Hello and... \nGoodbye!!");
    }
}
